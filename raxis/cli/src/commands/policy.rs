// raxis-cli::commands::policy — policy sign / generate-sidecar-secret subcommands.
//
// Normative reference: cli-ceremony.md §4.1 `policy sign` and kernel-store.md
// §2.5.3 "Plan artifact signing contract".
//
// Signs a TOML artifact (policy.toml or plan.toml) with the operator's
// private key and writes <artifact>.sig. No kernel connection required.
//
// Canonical signing scheme (the ONLY scheme accepted by the kernel):
//
//   signing_input = SHA-256("RAXIS-V1-PLAN" || 0x00 || artifact_bytes)
//   sig           = Ed25519Sign(operator_sk, signing_input)
//
// This routes through `raxis_crypto::plan::plan_signing_input` so that the
// CLI signer and the kernel verifier (`raxis_crypto::plan::verify_plan_signature`,
// called from `lifecycle::approve_plan`) agree on the byte-exact input. The
// previous implementation signed `sha256_hex(bytes).as_bytes()` (the UTF-8
// hex *string* of the digest) — review §2.2 catastrophic finding "plan
// signing semantic mismatch". No legacy operator key in the wild has yet
// signed real bundles under that scheme; we switch outright rather than
// carrying a compatibility branch.

use std::fs;
use std::path::PathBuf;

use raxis_crypto::plan::{plan_artifact_sha256, plan_signing_input};
use raxis_crypto::token::try_random_array;

use crate::errors::CliError;
use crate::GlobalFlags;

// ---------------------------------------------------------------------------
// `raxis policy generate-sidecar-secret` — V2_GAPS §C5 / extensibility-traits.md §9A.7C.
//
// Mints a 32-byte cryptographically-random shared secret for the
// HTTP-sidecar HMAC-SHA256 authentication scheme and prints it as
// 64 hex characters. The secret is **never** persisted to disk by
// this command — the operator copies it into both
//
//   1. `policy.toml` under the matching `[[providers]]` block as
//      `sidecar_hmac_secret = "<64-hex>"`, and
//   2. the sidecar process's environment, e.g.
//      `export RAXIS_SIDECAR_HMAC_SECRET="<64-hex>"`.
//
// Output format:
//
//   * Default: a single line of 64 hex characters followed by `\n`.
//     Pipe-friendly: `raxis policy generate-sidecar-secret > secret.hex`.
//   * `--json`: a single-line JSON object
//     `{"sidecar_hmac_secret":"<64-hex>"}` for tooling that already
//     parses doctor output as JSON.
//   * `--annotated` (mutually exclusive with `--json`): three-line
//     human-readable form with the literal `sidecar_hmac_secret` key
//     and a fingerprint of the secret (first 8 hex of SHA-256, so
//     operators can disambiguate which secret got copied where) for
//     paste-into-`policy.toml` ergonomics.
//
// We deliberately do NOT persist anywhere by default: the only place
// the kernel ever needs the secret is via `policy.toml`, which is
// the operator-signed source of truth. Auto-writing into
// `policy.toml` would (a) require operator re-signing the bundle
// before the next epoch-advance and (b) silently mutate a file that
// must round-trip through `git diff` for audit. The operator
// workflow per `extensibility-traits.md §9A.7C` is paste-driven.
// ---------------------------------------------------------------------------

pub fn run_generate_sidecar_secret(
    _flags: &GlobalFlags,
    args: &[String],
) -> Result<(), CliError> {
    let mut json = false;
    let mut annotated = false;

    for a in args {
        match a.as_str() {
            "--json" => json = true,
            "--annotated" => annotated = true,
            other => {
                return Err(CliError::Usage(format!(
                    "unknown `policy generate-sidecar-secret` flag: {other:?} \
                     (available: --json, --annotated)"
                )))
            }
        }
    }
    if json && annotated {
        return Err(CliError::Usage(
            "--json and --annotated are mutually exclusive".to_owned(),
        ));
    }

    let bytes: [u8; 32] = try_random_array()?;
    let hex_secret = hex_encode(&bytes);

    if json {
        println!("{{\"sidecar_hmac_secret\":\"{hex_secret}\"}}");
    } else if annotated {
        let fp = sidecar_secret_fingerprint(&hex_secret);
        println!("# Generated by `raxis policy generate-sidecar-secret`.");
        println!("# Fingerprint (first 8 hex of SHA-256): {fp}");
        println!("# Paste under the matching [[providers]] block in policy.toml,");
        println!("# then re-sign with `raxis policy sign policy.toml`.");
        println!("# Set the same value in the sidecar's RAXIS_SIDECAR_HMAC_SECRET env.");
        println!("sidecar_hmac_secret = \"{hex_secret}\"");
    } else {
        println!("{hex_secret}");
    }
    Ok(())
}

/// 64-character lowercase hex encoding without external deps. The
/// `hex` crate is already a workspace dep but we emit through this
/// trivial helper to keep `run_generate_sidecar_secret` self-contained.
fn hex_encode(bytes: &[u8]) -> String {
    const TBL: &[u8; 16] = b"0123456789abcdef";
    let mut out = String::with_capacity(bytes.len() * 2);
    for b in bytes {
        out.push(TBL[(b >> 4) as usize] as char);
        out.push(TBL[(b & 0x0f) as usize] as char);
    }
    out
}

/// First 8 lowercase hex chars of SHA-256(secret_bytes_as_hex_ascii).
/// Lets operators eyeball whether the secret they pasted into
/// `policy.toml` matches the one configured in the sidecar without
/// printing the secret itself.
fn sidecar_secret_fingerprint(hex_secret: &str) -> String {
    use sha2::{Digest, Sha256};
    let mut h = Sha256::new();
    h.update(hex_secret.as_bytes());
    let digest = h.finalize();
    let mut fp = String::with_capacity(8);
    const TBL: &[u8; 16] = b"0123456789abcdef";
    for b in &digest[..4] {
        fp.push(TBL[(b >> 4) as usize] as char);
        fp.push(TBL[(b & 0x0f) as usize] as char);
    }
    fp
}

pub fn run_sign(flags: &GlobalFlags, args: &[String]) -> Result<(), CliError> {
    let mut artifact_path: Option<PathBuf> = None;
    let mut key_path: Option<PathBuf> = None;
    let mut force_misconfig: bool = false;
    let mut i = 0;

    while i < args.len() {
        match args[i].as_str() {
            "--key" => {
                i += 1;
                key_path = Some(PathBuf::from(
                    args.get(i)
                        .ok_or_else(|| CliError::Usage("--key requires a path".to_owned()))?,
                ));
            }
            // Per cli-ceremony.md §4.1 `policy sign --force-misconfig`:
            // operator-explicit acknowledgement that the artifact contains
            // operator entries with `force_misconfig_bypass = true`. Without
            // this flag, signing such an artifact is refused — the bypass
            // must be visible in shell history / CI logs, not silently
            // hidden inside a TOML field.
            "--force-misconfig" => {
                force_misconfig = true;
            }
            arg if !arg.starts_with('-') && artifact_path.is_none() => {
                artifact_path = Some(PathBuf::from(arg));
            }
            other => return Err(CliError::Usage(format!("unknown policy sign flag: {other:?}"))),
        }
        i += 1;
    }

    let artifact_path = artifact_path
        .ok_or_else(|| CliError::Usage("policy sign requires <artifact.toml>".to_owned()))?;
    let key_path = key_path
        .or_else(|| flags.operator_key_path.clone())
        .ok_or_else(|| {
            CliError::Usage(
                "policy sign requires --key <path> or --operator-key global flag".to_owned(),
            )
        })?;

    // V2 hard-reject (plan-bundle-sealing.md §4.5): reject any attempt to
    // produce a V1-style sibling `plan.sig` for a `plan.toml` artifact.
    // `policy sign` remains the canonical signer for `policy.toml`,
    // delegation artifacts, and every other non-plan TOML, so the
    // narrowest reject we can make is to filter on the canonical
    // V1 plan filename. An operator who insists on signing a plan-shaped
    // artifact through this path would have to rename it, which is a
    // strong enough signal that they know they are leaving the V2 happy
    // path.
    //
    // The §4.5 migration guide tells operators their plan submission
    // path is `raxis submit plan <plan.toml>`, which signs in memory and
    // submits in the same IPC call — there is no on-disk `plan.sig`.
    if is_v1_plan_artifact_path(&artifact_path) {
        return Err(CliError::Usage(v1_plan_sign_removal_message(&artifact_path)));
    }

    // Read artifact bytes verbatim. Whitespace, BOM, trailing newline — the
    // signature is over the file as it sits on disk; the kernel will hash and
    // verify the same exact bytes.
    let artifact_bytes = fs::read(&artifact_path).map_err(|e| CliError::Io {
        path: artifact_path.display().to_string(),
        source: e,
    })?;

    // Step 7 (operator-cert ceremony) — refuse to sign a policy artifact
    // that opts into structural cert-misconfig bypass UNLESS the operator
    // explicitly passed `--force-misconfig`. We never block plan.toml or
    // any other artifact: the detector is intentionally conservative
    // (parses as a permissive TOML and only fires when there's at least
    // one operators.entries[*].force_misconfig_bypass = true).
    let bypass_entries = scan_for_misconfig_bypass(&artifact_bytes);
    if !bypass_entries.is_empty() && !force_misconfig {
        return Err(CliError::Usage(format!(
            "refusing to sign {}: {n} operator entr{ies_or_y} declare \
             force_misconfig_bypass = true (operators: {ops}). \
             Re-run with `--force-misconfig` to acknowledge the bypass; \
             the kernel will emit one OperatorCertMisconfigBypassed audit \
             event per relaxed structural rule at boot.",
            artifact_path.display(),
            n              = bypass_entries.len(),
            ies_or_y       = if bypass_entries.len() == 1 { "y" } else { "ies" },
            ops            = bypass_entries.join(", "),
        )));
    }
    if !bypass_entries.is_empty() {
        // --force-misconfig path: print a structured warning to stderr so
        // CI logs and shell scrollback both record the bypass at sign time.
        eprintln!(
            "{{\"level\":\"warn\",\"event\":\"policy_sign_misconfig_bypass\",\
             \"artifact\":\"{}\",\"bypassed_operators\":[{}]}}",
            artifact_path.display(),
            bypass_entries
                .iter()
                .map(|s| format!("\"{}\"", s.replace('"', "\\\"")))
                .collect::<Vec<_>>()
                .join(","),
        );
    }

    // Plain SHA-256 of the bytes (for `plan_sha256` field — surfaces in the
    // kernel's `signed_plan_artifacts.plan_bytes` and audit records).
    let sha256 = plan_artifact_sha256(&artifact_bytes);

    // Sign the canonical 32-byte SHA-256 of "RAXIS-V1-PLAN\x00" || artifact_bytes.
    // See module header — this MUST match what the kernel passes to
    // `Ed25519::verify` in `lifecycle::approve_plan`.
    let signing_key = crate::signing::load_operator_key(&key_path)?;
    let signing_input = plan_signing_input(&artifact_bytes);
    let sig_hex = crate::signing::sign_bytes(&signing_key, &signing_input);

    // Compute operator fingerprint.
    let pubkey_bytes = signing_key.verifying_key().to_bytes();
    let fingerprint = crate::conn::pubkey_fingerprint(&pubkey_bytes);

    // Write <artifact>.sig in TOML format.
    let sig_path = artifact_path.with_extension("sig");
    let sig_toml = format!(
        r#"# RAXIS artifact signature — generated by `raxis policy sign`
signed_by      = "{fingerprint}"
plan_sha256    = "{sha256}"
signature_hex  = "{sig_hex}"
signed_at      = {signed_at}
"#,
        signed_at = std::time::SystemTime::now()
            .duration_since(std::time::UNIX_EPOCH)
            .unwrap_or_default()
            .as_secs(),
    );
    fs::write(&sig_path, sig_toml.as_bytes()).map_err(|e| CliError::Io {
        path: sig_path.display().to_string(),
        source: e,
    })?;

    println!("✓ Signed {} → {}", artifact_path.display(), sig_path.display());
    println!("  fingerprint:  {fingerprint}");
    println!("  plan_sha256:  {sha256}");

    Ok(())
}


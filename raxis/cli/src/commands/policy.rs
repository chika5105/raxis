/// Returns `true` when the file's basename is the V1 plan convention
/// (`plan.toml`). Comparison is case-sensitive — the V1 contract was
/// case-sensitive too (`signed_plan_artifacts` keys on the literal
/// bytes), and Linux/macOS filesystems are typically case-sensitive.
/// An operator who genuinely needs to sign a plan-shaped artifact
/// through `policy sign` can rename it to e.g. `Plan.toml`; the
/// rename is itself a strong signal that they understand they are
/// leaving the V2 happy path.
pub(crate) fn is_v1_plan_artifact_path(path: &std::path::Path) -> bool {
    path.file_name()
        .and_then(|n| n.to_str())
        .map(|name| name == "plan.toml")
        .unwrap_or(false)
}

/// Operator-facing migration text. Pulled out of `run_sign` so the test
/// suite can pin the exact message — drift here is operator-visible
/// behaviour change and forces a corresponding spec update in
/// `plan-bundle-sealing.md §4.5`.
pub(crate) fn v1_plan_sign_removal_message(artifact_path: &std::path::Path) -> String {
    format!(
        "V1 `policy sign {artifact}` (plan-signing path) is removed in V2.\n\
         \n\
         The two-step `policy sign plan.toml` + `plan submit <id> <dir>`\n\
         workflow is replaced by V2.1 atomic sign+submit:\n\
         \n\
         \traxis submit plan {artifact} [--initiative-id <id>] \\\n\
         \t                                       [--no-dry-run]\n\
         \n\
         The V2 path signs the canonical plan bundle in memory and ships\n\
         it to the kernel in a single IPC call — no on-disk `plan.sig`,\n\
         no TOCTOU between sign and submit. `policy sign` remains the\n\
         canonical signer for `policy.toml`, delegation artifacts, and\n\
         every other non-plan TOML. See plan-bundle-sealing.md §4 for\n\
         the full ceremony and §4.5 for the migration guide.",
        artifact = artifact_path.display(),
    )
}

// ---------------------------------------------------------------------------
// scan_for_misconfig_bypass — best-effort detector for the
// `--force-misconfig` gate.
//
// We intentionally do NOT route through `raxis-policy::PolicyBundle::validate`:
// validation does signature checks, fingerprint checks, and bundle-wide
// invariants that haven't been enforced yet at sign time (the operator
// may be mid-edit). We just want to know "does this artifact carry at
// least one operator entry with `force_misconfig_bypass = true`?" — a
// permissive serde shape over the few fields we care about gives us
// exactly that, with zero false positives on plan.toml / non-policy
// artifacts (which never carry `[[operators.entries]]`).
//
// Returns the display_names (or fingerprints, if display_name is missing)
// of every entry that opted into bypass, in declaration order. Empty
// vector means "no bypass detected" — caller proceeds normally.
// ---------------------------------------------------------------------------
fn scan_for_misconfig_bypass(artifact_bytes: &[u8]) -> Vec<String> {
    #[derive(serde::Deserialize)]
    struct Probe {
        #[serde(default)]
        operators: Option<OperatorsBlock>,
    }
    #[derive(serde::Deserialize)]
    struct OperatorsBlock {
        #[serde(default)]
        entries: Vec<OperatorEntryProbe>,
    }
    #[derive(serde::Deserialize)]
    struct OperatorEntryProbe {
        #[serde(default)]
        display_name: Option<String>,
        #[serde(default)]
        pubkey_fingerprint: Option<String>,
        #[serde(default)]
        force_misconfig_bypass: bool,
    }

    let s = match std::str::from_utf8(artifact_bytes) {
        Ok(s) => s,
        Err(_) => return Vec::new(),
    };
    let parsed: Probe = match toml::from_str(s) {
        Ok(p) => p,
        // Not parseable as TOML, or the shape doesn't match — treat as
        // not-a-policy. The downstream signing step still runs because the
        // signature is over raw bytes; we only block when we can prove the
        // artifact opted into bypass.
        Err(_) => return Vec::new(),
    };
    let entries = parsed.operators.map(|b| b.entries).unwrap_or_default();
    entries
        .into_iter()
        .filter(|e| e.force_misconfig_bypass)
        .map(|e| {
            e.display_name
                .or(e.pubkey_fingerprint)
                .unwrap_or_else(|| "<unnamed>".to_owned())
        })
        .collect()
}


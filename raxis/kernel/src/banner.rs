//! Boot banner printed to stderr when the kernel starts.
//!
//! The ASCII art is a high-fidelity rendering of the RAXIS logo
//! (the stylized R with the cyan diagonal axis and "RAXIS —
//! REFERENCE MONITOR FOR AI" wordmark), converted to character

//!
//! The banner is colorized with ANSI escape codes:
//!   - Cyan (bold)  for the art — matches the brand's accent color
//!   - White (bold) for the version line
//!   - Dim white    for the subtitle
//!
//! At ~55 lines it's tall, but kernel boot is a one-time event and
//! the operator expects to see the identity confirmed before any
//! diagnostics scroll past.


/// Compact single-line banner for structured-log mode
/// (`RAXIS_LOG_FORMAT=json`). Emits a JSON object instead of
/// ASCII art.
pub fn print_boot_banner_json() {
    eprintln!(
        "{{\"level\":\"info\",\"event\":\"KernelBoot\",\"version\":\"{}\"}}",
        env!("CARGO_PKG_VERSION"),
    );
}
